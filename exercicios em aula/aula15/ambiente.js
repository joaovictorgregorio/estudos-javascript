let num = [5, 2, 89, 45, 74, 6]

// Adiciona valores no array, a posição do valor depende da ordem que o programador coloca o push para ser executado
num.push(1)
// Ordena o array em ordem crescente do menor para o maior
num.sort()
console.log(num)
// num.length informa o valor do array
console.log(`O vetor tem ${num.length} posições`) 
console.log(`O primeiro valor do vetor é ${num[0]}`)